#This is our varible 'fruits'
#we store data or in this case fruits in a list example 'Apple'
fruits = [ "Apple", "Peach", "Pear" ]
#this is our for loop. for loops define varibles in lists items for us automaticlly
for fruit in fruits:
    #This set of code runs over and over until the loop of instructions is completed.
    print(fruit)
    print(fruit + "pie")
    print(fruits)